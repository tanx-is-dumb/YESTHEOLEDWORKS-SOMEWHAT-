enum layers {
    _DEFAULT,
    _VALORANT,
    _GENSHIN
};

static uint8_t current_layer = _DEFAULT;

void cycle_layers(void) {
    // Increment the layer
    current_layer++;
    if (current_layer > _GENSHIN) {
        current_layer = _DEFAULT;
    }
    layer_move(current_layer);
}

